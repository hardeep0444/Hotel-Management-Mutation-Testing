package org.example;
import Algorithms.BackTracking.Sodoku;
import org.junit.Test;

import java.util.List;

import static org.junit.Assert.*;

public class SudokuTest {
    @Test
    public void testSolveSudoku() {
        Sodoku obj = new Sodoku();

        // Test case with a valid Sudoku puzzle
        char[][] sudokuBoard1 = {
                {'5', '3', '.', '.', '7', '.', '.', '.', '.'},
                {'6', '.', '.', '1', '9', '5', '.', '.', '.'},
                {'.', '9', '8', '.', '.', '.', '.', '6', '.'},
                {'8', '.', '.', '.', '6', '.', '.', '.', '3'},
                {'4', '.', '.', '8', '.', '3', '.', '.', '1'},
                {'7', '.', '.', '.', '2', '.', '.', '.', '6'},
                {'.', '6', '.', '.', '.', '.', '2', '8', '.'},
                {'.', '.', '.', '4', '1', '9', '.', '.', '5'},
                {'.', '.', '.', '.', '8', '.', '.', '7', '9'}
        };
        obj.solveSudoku(sudokuBoard1);
        assertEquals(true,obj.solveSudoku(sudokuBoard1));

        // Test case with an invalid Sudoku puzzle
        char[][] sudokuBoard2 = {
                {'5', '3', '.', '.', '7', '.', '.', '.', '.'},
                {'6', '.', '.', '1', '9', '5', '.', '.', '.'},
                {'.', '9', '8', '.', '.', '.', '.', '6', '.'},
                {'8', '.', '.', '.', '6', '.', '.', '.', '3'},
                {'4', '.', '.', '8', '.', '3', '.', '.', '1'},
                {'7', '.', '.', '.', '2', '.', '.', '.', '6'},
                {'.', '6', '.', '.', '.', '.', '2', '8', '.'},
                {'.', '.', '.', '4', '1', '9', '.', '.', '5'},
                {'.', '.', '.', '.', '8', '.', '.', '7', '8'} // Invalid value
        };
        assertEquals(false,obj.solveSudoku(sudokuBoard2));
    }
}